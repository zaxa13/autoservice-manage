def seed_works():
    db = SessionLocal()
    try:
        existing_count = db.query(Work).count()
        if existing_count > 0:
            print(f"В базе уже есть {existing_count} работ.")
            answer = input("Добавить каталог поверх? (y/n): ").strip().lower()
            if answer != "y":
                print("Отменено.")
                return

        added = 0
        for name, description, price, duration, category in WORKS_CATALOG:
            # Проверяем, нет ли уже такой работы
            existing = db.query(Work).filter(Work.name == name).first()
            if existing:
                continue

            work = Work(
                name=name,
                description=description,
                price=price,
                duration_minutes=duration,
                category=category,
            )
            db.add(work)
            added += 1

        db.commit()
        print(f"Добавлено {added} работ в каталог.")
        print(f"Всего работ в базе: {db.query(Work).count()}")

        # Статистика по категориям
        print("\nПо категориям:")
        for cat in WorkCategory:
            count = db.query(Work).filter(Work.category == cat).count()
            if count > 0:
                print(f"  {cat.value}: {count}")

    finally:
        db.close()


if __name__ == "__main__":
    seed_works()
